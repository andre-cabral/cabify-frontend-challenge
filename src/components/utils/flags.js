export function getFlagsImagesList(codeList) {
  const flagListComplete = getFlagList();

  return Object.keys(flagListComplete)
  .filter(code => codeList.includes(code))
  .reduce((usedFlags, code) => {
    usedFlags[code] = flagListComplete[code];
    return usedFlags;
  }, {});
}

